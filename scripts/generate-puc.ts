sql += `RETURNS void AS $$\n`
sql += `BEGIN\n`

// Sort by hierarchy so parents come before children
const sorted = [...puc].sort((a, b) => a.nivel - b.nivel || a.codigo.localeCompare(b.codigo))

for (const entry of sorted) {
  const padre = entry.padre ? `'${entry.padre}'` : 'NULL'
  sql += `  INSERT INTO puc_cuentas (codigo, emp_ide, nombre, tipo_naturaleza, nivel, padre) VALUES ('${entry.codigo}', p_emp_ide, '${entry.nombre.replace(/'/g, "''")}', '${entry.tipo_naturaleza}', ${entry.nivel}, ${padre});\n`
}

sql += `END;\n`
