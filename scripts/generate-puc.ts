interface PucEntry {
  codigo: string
  nombre: string
  tipo_naturaleza: 'D' | 'C'
  nivel: number
  padre: string | null
}

const puc: PucEntry[] = []

function add(codigo: string, nombre: string, nat: 'D' | 'C', nivel: number) {
  const padre =
    nivel === 1 ? null
    : nivel === 2 ? codigo[0]
    : nivel === 4 ? codigo.slice(0, 2)
    : nivel === 6 ? codigo.slice(0, 4)
    : null
  puc.push({ codigo, nombre, tipo_naturaleza: nat, nivel, padre })
}

